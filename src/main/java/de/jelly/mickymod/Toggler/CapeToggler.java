package de.jelly.mickymod.Toggler;

public class CapeToggler {

    public static String capevar;

    public static void toggleDefaultCape() {

        capevar = "defaultcape";

    }

    public static void toggleRainerStinklerCape() {

        capevar = "winkler";

    }

    public static void toggleNotchCape() {

        capevar = "notchcape";

    }

    public static void toggleFoxCape() {

        capevar = "foxcape";

    }

    public static void toggleAxoBucketCape() {

        capevar = "axobucketcape";

    }

}
